def get_dist_matrix(diagonal_val: float = 0.0) -> np.ndarray:
    mat = GENETIC_DIST_MATRIX.copy()
    np.fill_diagonal(mat, diagonal_val)
    return mat


def get_target_tensor(dist_matrix: np.ndarray, device) -> torch.Tensor:
    return torch.from_numpy(dist_matrix).to(device)           # (N, N)


def identify_by_distance(
    pred_vec: np.ndarray,
    gt_matrix: np.ndarray,
    metric: str = "cosine",
) -> int:
    if metric == "cosine":
        norm_p = pred_vec / (np.linalg.norm(pred_vec) + 1e-8)
        norm_m = gt_matrix / (np.linalg.norm(gt_matrix, axis=1, keepdims=True) + 1e-8)
        dists = 1.0 - norm_m @ norm_p          # khoảng cosine ∈ [0, 2]
    else:
        dists = np.linalg.norm(gt_matrix - pred_vec[None, :], axis=1)

    return int(np.argmin(dists))
